package day2;

public class P5_Comprasion {

	public static void main(String[] args) {
		/* 주의사항
		 * >= 을 반대로 쓰면 안됨
		 * <= 을 반대로 쓰면 안됨
		 * ==과 =을 혼동하지 말자
		 * 결과값이 참 또는 거짓.
		 * 비교 연산자는 연산자를 기준으로 양쪽에 어떤수가 나옴
		 * */
		int score = 95;
		System.out.println(score > 95);
		System.out.println(score >= 95);
		System.out.println(score < 95);
		System.out.println(score <= 95);
		System.out.println(score == 95);
		System.out.println(score != 95);
	}

}
